<h1>Svelte Scaffolding</h1>

<p>Tool to create a scaffolding for Svelte Apps.</p>

<ul>
	<li>Go to build</li>
	<li>Add Stuff</li>
	<li>Click download</li>
</ul>

<p>You will now have a zip file that you can dump in your src folder.</p>

<p>Endpoint that have parameters come with some code to read those parameters.</p>

